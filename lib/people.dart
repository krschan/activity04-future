class People {
  final String name;
  final int height;
  final String gender;
  final String birth;

  People(this.name, this.height, this.gender, this.birth);

  String getName() {
    return name;
  }

  int getHeight() {
    return height;
  }

  String getGender() {
    return gender;
  }

  String getBirth() {
    return birth;
  }
}
